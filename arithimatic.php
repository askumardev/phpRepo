function add($x, $y){
  return $x + $y;
}

function sub($x, $y){
  return $x - $y;
}

function multiply($x, $y){
  return $x * $y;
}

function divide($x, $y){
  return $x * $y;
}

function mod($x, $y){
  return $x % $y;
}

function expo($x, $y){
  return $x ** $y;
}

echo add(20, 10);
echo "\n";
echo sub(20, 10);
echo "\n";
echo multiply(20, 10);
echo "\n";
echo divide(20, 10);
echo "\n";
echo mod(20, 10); # modulo division
echo "\n";
echo expo(20, 10); #Exponentiation